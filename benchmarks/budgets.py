"""budgets.toml — loading, evaluation, and the ratchet's `--propose` refresh.

The two-tier performance contract (docs/design/benchmarks.md): `enforced` rows fail
`check`, `target` rows are reported loudly and never fail. Every failure mode is a
distinct, tested decay channel: violation, metric missing, predicate unevaluable,
python-provenance mismatch on CI. This module is gate code and sits under the 100%
coverage floor.
"""

from __future__ import annotations

import io
import math
import tomllib
from dataclasses import dataclass, field
from typing import Any, Literal

import tomli_w

from .results import Results, python_major_minor

Tier = Literal["enforced", "target"]
Outcome = Literal[
    "passed",
    "violated",
    "metric-missing",
    "not-applicable",
    "predicate-unevaluable",
    "python-mismatch",
]

# Ratchet ceilings sitting this far under their bound trigger the "tighten it" nudge.
STALE_FRACTION = 0.85

_ALLOWED_KEYS = {
    "metric",
    "max",
    "tier",
    "ratchet",
    "headroom",
    "profile",
    "platform",
    "ci_only",
    "context",
    "note",
}

_HEADER = """\
# The performance contract (docs/design/benchmarks.md — "Budgets").
#
# tier = "enforced": `benchmarks check` fails on violation. Deterministic or
#   ratchet-safe metrics only; every row carries context = provenance for its bound.
# tier = "target": the aspirational contract — reported on every run, never failing
#   CI until a future PR deliberately moves the row to enforced.
# ratchet = true: the bound derives from a measured value + headroom, and
#   `check --propose` refreshes it (from the CI artifact — never a local run; the
#   module census is python-version-dependent). Non-ratchet bounds are hand-set
#   contract values that propose leaves alone.
#
# This file is regenerated by `uv run python -m benchmarks check --propose`; keep
# prose in `note` fields, not comments below this header.
"""


class BudgetsError(ValueError):
    """budgets.toml (or a propose request) is malformed. The message names the row."""


@dataclass(frozen=True)
class Budget:
    metric: str
    max_value: float
    tier: Tier
    ratchet: bool = False
    headroom: float = 0.10
    profile: str | None = None
    platform: str | None = None
    ci_only: bool = False
    context: dict[str, str] = field(default_factory=dict)
    note: str = ""


@dataclass(frozen=True)
class RowResult:
    budget: Budget
    outcome: Outcome
    value: float | None = None
    detail: str = ""
    stale: bool = False

    @property
    def failed(self) -> bool:
        """Whether this row fails `check` — only enforced rows ever do."""
        return self.budget.tier == "enforced" and self.outcome in (
            "violated",
            "metric-missing",
            "predicate-unevaluable",
            "python-mismatch",
        )


@dataclass(frozen=True)
class Report:
    rows: list[RowResult]

    @property
    def failures(self) -> list[RowResult]:
        return [r for r in self.rows if r.failed]

    @property
    def enforced_evaluated(self) -> int:
        """Enforced rows that reached a verdict (anything but not-applicable) — the
        count `--require-enforced` guards against silently hitting zero."""
        return sum(
            1 for r in self.rows if r.budget.tier == "enforced" and r.outcome != "not-applicable"
        )


# ---------------------------------------------------------------- loading


def load_budgets(text: str) -> list[Budget]:
    try:
        doc = tomllib.loads(text)
    except tomllib.TOMLDecodeError as exc:
        raise BudgetsError(f"budgets.toml is not valid TOML: {exc}") from exc
    unknown_top = set(doc) - {"budget"}
    if unknown_top:
        raise BudgetsError(f"unknown top-level keys: {sorted(unknown_top)}")
    rows = doc.get("budget")
    if not isinstance(rows, list) or not rows:
        raise BudgetsError("no [[budget]] rows")
    return [_load_row(i, row) for i, row in enumerate(rows)]


def _load_row(index: int, row: object) -> Budget:
    where = f"[[budget]] #{index + 1}"
    if not isinstance(row, dict):
        raise BudgetsError(f"{where}: expected a table")
    unknown = set(row) - _ALLOWED_KEYS
    if unknown:
        raise BudgetsError(f"{where}: unknown keys {sorted(unknown)}")
    metric = row.get("metric")
    if not isinstance(metric, str) or not metric:
        raise BudgetsError(f"{where}: metric must be a non-empty string")
    where = f"[[budget]] {metric}"
    bound = row.get("max")
    if isinstance(bound, bool) or not isinstance(bound, int | float):
        raise BudgetsError(f"{where}: max must be a number")
    tier_node = row.get("tier")
    if tier_node not in ("enforced", "target"):
        raise BudgetsError(f"{where}: tier must be 'enforced' or 'target'")
    tier: Tier = "enforced" if tier_node == "enforced" else "target"
    ratchet = row.get("ratchet", False)
    if not isinstance(ratchet, bool):
        raise BudgetsError(f"{where}: ratchet must be a boolean")
    if ratchet and tier != "enforced":
        raise BudgetsError(f"{where}: ratchet rows must be enforced")
    headroom = row.get("headroom", 0.10)
    if isinstance(headroom, bool) or not isinstance(headroom, int | float):
        raise BudgetsError(f"{where}: headroom must be a number")
    if not 0 < headroom < 1:
        raise BudgetsError(f"{where}: headroom must be between 0 and 1")
    profile = _optional_str(row, "profile", where)
    platform = _optional_str(row, "platform", where)
    ci_only = row.get("ci_only", False)
    if not isinstance(ci_only, bool):
        raise BudgetsError(f"{where}: ci_only must be a boolean")
    context_node = row.get("context", {})
    if not isinstance(context_node, dict) or not all(
        isinstance(k, str) and isinstance(v, str) for k, v in context_node.items()
    ):
        raise BudgetsError(f"{where}: context must be a table of strings")
    if tier == "enforced" and not context_node:
        raise BudgetsError(f"{where}: enforced rows require context (provenance)")
    note = row.get("note", "")
    if not isinstance(note, str):
        raise BudgetsError(f"{where}: note must be a string")
    return Budget(
        metric=metric,
        max_value=float(bound),
        tier=tier,
        ratchet=ratchet,
        headroom=float(headroom),
        profile=profile,
        platform=platform,
        ci_only=ci_only,
        context={str(k): str(v) for k, v in context_node.items()},
        note=note,
    )


def _optional_str(row: dict[Any, Any], key: str, where: str) -> str | None:
    value = row.get(key)
    if value is None:
        return None
    if not isinstance(value, str) or not value:
        raise BudgetsError(f"{where}: {key} must be a non-empty string")
    return value


# ---------------------------------------------------------------- evaluation


def evaluate(budgets: list[Budget], results: Results) -> Report:
    return Report(rows=[_evaluate_row(b, results) for b in budgets])


def _evaluate_row(budget: Budget, results: Results) -> RowResult:
    meta = results.meta
    # Predicates first. An absent-or-empty meta field a predicate needs is a decay
    # channel, not a pass (JSON null ci_runner is *evaluable*: it means "not CI").
    if budget.profile is not None and budget.profile != meta.profile:
        return RowResult(budget, "not-applicable", detail=f"profile is {meta.profile}")
    if budget.platform is not None:
        if not meta.host.platform_key:
            return RowResult(
                budget, "predicate-unevaluable", detail="meta.host.platform_key is empty"
            )
        if budget.platform != meta.host.platform_key:
            return RowResult(
                budget, "not-applicable", detail=f"platform is {meta.host.platform_key}"
            )
    on_ci = meta.host.ci_runner is not None
    if on_ci and not meta.host.ci_runner:
        return RowResult(budget, "predicate-unevaluable", detail="meta.host.ci_runner is empty")
    if budget.ci_only and not on_ci:
        return RowResult(budget, "not-applicable", detail="not a CI run")
    # Python provenance (enforced rows carrying a python context, major.minor).
    pinned = budget.context.get("python", "")
    if budget.tier == "enforced" and pinned:
        actual = python_major_minor(meta.python)
        if pinned != actual:
            if on_ci:
                return RowResult(
                    budget,
                    "python-mismatch",
                    detail=f"bound set on python {pinned}, CI runs {actual}",
                )
            return RowResult(
                budget,
                "not-applicable",
                detail=f"bound pinned to python {pinned}; this host runs {actual}",
            )
    metric = results.metrics.get(budget.metric)
    if metric is None:
        return RowResult(budget, "metric-missing", detail="metric absent from results")
    if metric.value > budget.max_value:
        return RowResult(
            budget,
            "violated",
            value=metric.value,
            detail=f"{metric.value:g} {metric.unit} > {budget.max_value:g}",
        )
    stale = budget.ratchet and metric.value < STALE_FRACTION * budget.max_value
    return RowResult(
        budget,
        "passed",
        value=metric.value,
        detail=f"{metric.value:g} {metric.unit} ≤ {budget.max_value:g}",
        stale=stale,
    )


_SYMBOLS: dict[Outcome, str] = {
    "passed": "ok",
    "violated": "FAIL",
    "metric-missing": "FAIL",
    "predicate-unevaluable": "FAIL",
    "python-mismatch": "FAIL",
    "not-applicable": "n/a",
}


def render_report(report: Report) -> str:
    """The human check output: one line per row, a tally, and the stale nudges. The
    machine contract is the exit code plus `--require-enforced`, never this text."""
    lines: list[str] = []
    for row in report.rows:
        mark = (
            _SYMBOLS[row.outcome]
            if row.budget.tier == "enforced"
            else ("ok" if row.outcome == "passed" else "△")
        )
        lines.append(
            f"[{row.budget.tier}] {mark:>4}  {row.budget.metric}: "
            f"{row.outcome}{' — ' + row.detail if row.detail else ''}"
        )
        if row.stale:
            lines.append(
                f"[enforced] note  {row.budget.metric}: measured {row.value:g} sits below "
                f"{STALE_FRACTION:.0%} of the bound {row.budget.max_value:g} — "
                "ceiling is stale, tighten it (check --propose)"
            )
    enforced = [r for r in report.rows if r.budget.tier == "enforced"]
    passed = sum(1 for r in enforced if r.outcome == "passed")
    lines.append(
        f"enforced: {len(enforced)} rows, {report.enforced_evaluated} evaluated, "
        f"{passed} passed, {len(report.failures)} failed · "
        f"target: {sum(1 for r in report.rows if r.budget.tier == 'target')} rows"
    )
    return "\n".join(lines) + "\n"


# ---------------------------------------------------------------- propose


def propose(budgets: list[Budget], results: Results) -> str:
    """The refreshed budgets.toml: ratchet rows get max = measured x (1 + headroom)
    (ceil — bounds are integers in spirit) and fresh context from the results
    manifest; every other row round-trips untouched. Output is the complete file."""
    refreshed: list[Budget] = []
    for budget in budgets:
        if not budget.ratchet:
            refreshed.append(budget)
            continue
        metric = results.metrics.get(budget.metric)
        if metric is None:
            raise BudgetsError(f"cannot propose {budget.metric}: metric absent from results")
        refreshed.append(
            Budget(
                metric=budget.metric,
                max_value=float(math.ceil(metric.value * (1 + budget.headroom))),
                tier=budget.tier,
                ratchet=True,
                headroom=budget.headroom,
                profile=budget.profile,
                platform=budget.platform,
                ci_only=budget.ci_only,
                context={
                    "python": python_major_minor(results.meta.python),
                    "commit": results.meta.git.commit,
                    "date": results.meta.generated_at.split("T")[0],
                },
                note=budget.note,
            )
        )
    return render_budgets(refreshed)


def render_budgets(budgets: list[Budget]) -> str:
    """Serialize rows back to TOML under the fixed header. Optional keys are emitted
    only when they carry information, so the canonical file stays diff-small."""
    rows: list[dict[str, object]] = []
    for b in budgets:
        row: dict[str, object] = {
            "metric": b.metric,
            "max": int(b.max_value) if b.max_value == int(b.max_value) else b.max_value,
            "tier": b.tier,
        }
        if b.ratchet:
            row["ratchet"] = True
            row["headroom"] = b.headroom
        if b.profile is not None:
            row["profile"] = b.profile
        if b.platform is not None:
            row["platform"] = b.platform
        if b.ci_only:
            row["ci_only"] = True
        if b.context:
            row["context"] = b.context
        if b.note:
            row["note"] = b.note
        rows.append(row)
    buffer = io.BytesIO()
    tomli_w.dump({"budget": rows}, buffer)
    return _HEADER + "\n" + buffer.getvalue().decode("utf-8")
